def prueba():
    print("Hola Mundo")
    print("Hoy es Domingo")

print(prueba())

def saludo(idioma):
    if idioma == "en":
        print("Hello")
    elif idioma == "es":
        print("Hola")
    elif idioma == "fr":
        print("Bonjour")

print(saludo("en"))
print(saludo("es"))
print(saludo("fr"))

